"""
    课间十分钟
        两人都是数学爱好者，便发明了一个游戏。
        在游戏开始前，他们先约定一个正整数n，同时令m=1。
        游戏过程中，每个人都可以将m的值扩大2到9中的任意倍数。
        第一个使m≥n的人就是最后的赢家。
        181818181818和ZBT都十分聪明，并且游戏过程中都使用最佳的策略，问谁会是最后的赢家。（181818181818总是第一个先玩）
    输入：一个正整数n（1≤n≤2^32-1）
    输出：在使用最佳的策略的情况下，如果181818181818必胜，则输出“181818181818”。如果ZBT必胜，则输出“ZBT”。
"""


n = int(input())
m = 1
i = 0
result = 0

if n == 1:
    print('181818181818')
else:
    while m <= n:
        begin = 18**i
        m = begin*9
        i += 1
        if begin < n <= m:
            result = 1

    if result:
        print('181818181818')
    else:
        print('ZBT')




